package src2;

public class Primitives {
    public static void main(String[] args) {
        //  Primitives data Types
        //  Any Data type that you cannot break any further
        //  Examples: Integer, Character, Float, Double, Long, Boolean

        int rollno = 64;                 // 4 bytes
        char letter = 'r';
        float marks = 98.65f;            // 4 bytes
        double num = 45673846.66483646;  // Large decimal numbers. 8 bytes
        long integer = 37647877794794L;  // Long integer number. 8 bytes
        boolean check = false;           // true and false values.

        // All decimal values are by default double
        // if we have to store in float we have to add 'f' at the end of the value
        // Ctrl + slash = Comment out selected data
        // All the primitive data types contains classes as well.
        // primitive data type is with lowercase but class is with uppercase
        // These are called wrapper classes for additional functionalities
        // for e.g:

        Integer rno = 64;
        // rno. gives a lot of functions/ methods

        // A wrapper class is a class that wraps one or more primitive data types.
        // Wrapper classes are very common in java.
        // For example, String, Integer and Boolean are wrapper classes.
        // Wrapper classes are used to treat primitive data types as objects.










    }
}
